// Дан неотсортированный слайс целых чисел. Напишите функцию Clean(nums []int, x int) ([]int),
// которая удаляет из исходного слайса все вхождения x.
// Важно сохранить порядок следования элементов и не использовать дополнительный слайс.

package main

func Clean(nums []int, x int) []int {
	// Счётчик для позиции, куда вставлять следующий "хороший" элемент
	j := 0

	// Проходим по всем элементам
	for i := 0; i < len(nums); i++ {
		if nums[i] != x {
			// Если элемент не равен x, записываем его на позицию j
			nums[j] = nums[i]
			j++ // Увеличиваем позицию для записи
		}
	}

	// Возвращаем слайс обрезанный до нужной длины
	return nums[:j]
}
