func twoSum(nums []int, target int) []int {
	// Создаем карту для хранения индексов уже просмотренных элементов
	seen := make(map[int]int)

	// Проходим по каждому элементу массива
	for i, num := range nums {
		// Вычисляем необходимое число для достижения цели
		diff := target - num

		// Проверяем, есть ли это число в карте
		if index, found := seen[diff]; found {
			return []int{index, i} // Возвращаем индексы найденной пары
		}

		// Если не нашли, сохраняем текущий элемент и его индекс в карте
		seen[num] = i
	}

	return nil // На случай, если решение не найдено (но по условию оно всегда есть)
}
